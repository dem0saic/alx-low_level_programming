int main(void)
{
	char letter = 'a';
	int num = 0;

	while
		(num < 10) {
			putchar(num + '0');
			num++;
		}
	while
		(letter < 'g') {
			putchar(letter);
			letter++;
		}
		putchar('\n');
		return (0);
}

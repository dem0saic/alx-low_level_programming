/**
* add - add two integers
* @a: first int
* @b: second int
* Return: addition
*/
int add(int a, int b){
	return (a + b);
};

/**
* sub - subtract two integers
* @a: first int
* @b: second int
* Return: subtracted final result
*/
int sub(int a, int b){
	return (a - b);
};

/**
* mul - multiply two integers
* @a: first int
* @b: second int
* Return: multiply
*/
int mul(int a, int b){
	return (a * b);
};

/**
* mod - modules of two integers
* @a: first int
* @b: second int
* Return: modules
*/
int mod(int a, int b){
	return (a % b);
};
		
/**
* div - divide two integers
* @a: first int
* @b: second int
* Return: Division
*/
int div(int a, int b){
	return (a / b);
};
